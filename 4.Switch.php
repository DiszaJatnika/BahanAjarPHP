<?php 
    /**
     * switch memiliki fungsi yang sama dengan if else. 
     * bisa menjadi alternatif lain pengganti if else
     */
    $a = 3;
     switch($a){
        case 1:
            echo "Satu";
            break;
        case 2:
            echo "Dua";
            break;
        case 3:
            echo "Tiga";
            break;
        default:
            echo "Test";
     }